                        </div>
                        <div class="d-flex align-items-center">
                            <i class="ti-check display-26 text-primary"></i>
                            <span class="ms-3 text-dark">The temperature control has a decisive influence on the quality of the pre-pressed glass. if the glass temperature is high, the glass and film are prematurely bonded together, and the edges and the back are sealed too early. The air is difficult to remove and form bubbles, if the temperature is too low, which will reduce the adhesion of the film to the glass, so that the film and the glass are not firmly bonded, the edge sealing is not good, and the external gas enters the film during the pressing process; Therefore, it is required to control the temperature appropriately. It is necessary to adjust the temperature of each zone according to the season and climate conditions, and to ensure the temperature of the glass. The pre-pressed laminated glass is a semi-finished product. Place it on the glass rack and sent into the autoclave for processing as soon as possible. The glass cannot be placed in the atmosphere for a long time; otherwise, the moisture will enter the interior of the laminated glass through the film, causing bubbles in the laminated glass. </span>
                        </div>

                        <p class="mb-lg-1-9">Tempered glass is a type of safety glass processed by controlled thermal or chemical treatments to increase its strength compared with normal glass. Tempering puts the outer surfaces into compression and the interior into tension. Such stresses cause the glass, when broken, to shatter into small granular chunks instead of splintering into jagged shards as ordinary annealed glass does. The granular chunks are less likely to cause injury.</p>

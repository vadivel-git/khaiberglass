                        <h2 class="mb-3 h3">Laminated Glass</h2>
                        <p class="mb-lg-1-9">Laminated glass is a type of Safety Glass and is made by pasting PVB (polyvinyl butyral) resin glue film between two or more pieces of glass sheet, then heating, pressing and bonding them together to create flat or curved compound glass product.</p>
                        <p class="mb-lg-1-9">Laminated glass is extra strong and extremely secure. It can provide UV protection against the harmful ultraviolet rays that fade furnishings. It’s hard to break. When broken, it will shatter but still hold its form, minimizing the risk of injury. It is durable and versatile.  The thickness of the combined glass and the space between each pane can act as insulation and provide sound reduction.</p>
